package loops;

public class While_loop_study 
{
	public static void main(String[] args) {
		
		// I want print "2" 10 times
		// initialization-->1
		//condition-->10
		//updation--> incremental++
		
		
		int i=1;//initialization
		while(i<=10)//condition//1
		{
			System.out.println(2);
			i++;//updation
		}
		System.out.println("=================");
		

	}

}
